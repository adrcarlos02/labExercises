// Define an array

let planets = ['Venus', 'Pluto', 'Mars', 'Jupiter', 'Neptune'];

//Replace value of Array
planets[1] = 'Earth';
planets[4] = 'Saturn';

//Add a new element at the beginning of the Array
planets.unshift('Mercury');

//Remove last element at the end of the Array
planets.pop();

//Print the Array in the console
console.log(planets);
